-- Create banks table for storing bank information
CREATE TABLE IF NOT EXISTS banks (
    bankCode VARCHAR(6) UNIQUE NOT NULL,
    cbnCode VARCHAR(6) UNIQUE NOT NULL,
    name VARCHAR(255) NOT NULL,
    logo_filename VARCHAR(255),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Create index on code for fast lookups
CREATE INDEX IF NOT EXISTS idx_banks_bankCode ON banks(bankCode);
CREATE INDEX IF NOT EXISTS idx_banks_cbnCode ON banks(cbnCode);

